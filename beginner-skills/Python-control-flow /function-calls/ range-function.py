#  iterate over a sequence of numbers,

for i in range(5):
    print(i)

for i in range(5, 10):
    print(i)

#step
for i in range(0, 10, 3):
    print(i)

for i in range(-10, -100, -30):
    print(i)
# iterate over the indices of a sequence,
a = ['Mary', 'had', 'a', 'little', 'lamb']

for i in range(len(a)):
    print(i, a[i])

